# Una tienda desea calcular el total de una compra de tres productos. Se deben ingresar los precios de cada uno y mostrar el total a pagar.

producto1 = float(input("Ingrese costo primer producto:"));
producto2 = float(input("Ingrese costo segundo producto:"));
producto3 = float(input("Ingrese costo tercer producto:"));

total= producto1 + producto2 + producto3;
print(f"el costo total de lacompra es: {total}")
